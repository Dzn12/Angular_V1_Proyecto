export class Book {
  id: number = 0;
  titulo: string = '';
  genero: string = '';
  autor: string = '';

  constructor(id: number = 0, titulo: string = '', genero: string = '', autor: string = '') {
    this.id = id;
    this.titulo = titulo;
    this.genero = genero;
    this.autor = autor;
  }



  
}

  
